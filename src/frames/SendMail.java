private void wtf(){
            try
        {
            // Propiedades de la conexión
            Properties props = new Properties();
         //   props.setProperty("mail.smtp.host", txtServer.getText().trim());
            props.setProperty("mail.smtp.starttls.enable", "true");
            props.setProperty("mail.smtp.port", "587");
            props.setProperty("mail.smtp.user", txtUser.getText().trim());
            props.setProperty("mail.smtp.auth", "true");

            // Preparamos la sesion
            Session session = Session.getDefaultInstance(props);

            // Construimos el mensaje
            MimeMessage message = new MimeMessage(session);
            message.setFrom(new InternetAddress(txtDestino.getText().trim()));
            message.addRecipient(
                Message.RecipientType.TO,
                new InternetAddress(txtUser.getText().trim()));
            message.setSubject(txtAsunto.getText());
            message.setText(jTextArea1.getText());

            // Lo enviamos.
            Transport t = session.getTransport("smtp");
            t.connect(txtUser.getText().trim(), txtPass.getText());
            t.sendMessage(message, message.getAllRecipients());

            // Cierre.
            t.close();
        }
        catch (MessagingException e)
        {
            JOptionPane.showMessageDialog(this, e.getMessage(),"Atención",JOptionPane.WARNING_MESSAGE);
        }
}
    
